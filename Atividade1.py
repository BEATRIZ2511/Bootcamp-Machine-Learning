# 1. Explique, com suas palavras, o que é machine learning?
# R: Machine Learning é um campo da Inteligência Artificial em que sistemas
# aprendem a partir de dados, identificando padrões e melhorando seu desempenho
# em tarefas específicas sem serem programados explicitamente para isso.

# 2. Explique o conceito de conjunto de treinamento, conjunto de validação e
# conjunto de teste em machine learning.
# R:- Conjunto de treinamento: usado para ensinar o modelo, fazendo-o aprender os padrões dos dados.
# - Conjunto de validação: usado para ajustar o modelo durante o treinamento, escolhendo parâmetros e evitando overfitting.
# - Conjunto de teste: usado apenas no final, para avaliar o desempenho real do modelo em dados novos.

# 3. Explique como você lidaria com dados ausentes em um conjunto de dados de treinamento.
# R: - Remover linhas ou colunas com poucos valores ausentes, se não impactar a análise.
# - Preencher os valores ausentes (ex: média, mediana, moda ou técnicas mais avançadas).
# - Utilizar algoritmos que lidam bem com dados faltantes.

# 4. O que é uma matriz de confusão e como ela é usada para avaliar o desempenho de um modelo preditivo?
# R: A matriz de confusão é uma tabela que compara os valores reais com as previsões feitas pelo modelo.
# Ela mostra quantas previsões foram corretas e incorretas, dividindo os resultados em:
# - Verdadeiros Positivos (VP), Falsos Positivos (FP), Verdadeiros Negativos (VN) e Falsos Negativos (FN).
# A partir dela, podemos calcular métricas como acurácia, precisão, recall e F1-score,
# que ajudam a avaliar a qualidade do modelo preditivo.

